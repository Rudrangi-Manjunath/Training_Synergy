public class student1 {
        Integer roll_no;
        String name;
        Integer marks;

    public student1(Integer roll_no, String name, Integer marks) {
        this.roll_no = roll_no;
        this.name = name;
        this.marks = marks;
    }

    public Integer getRoll_no() {
        return roll_no;
    }

    public String getName() {
        return name;
    }

    public Integer getMarks() {
        return marks;
    }


}
